def replace_non_letter_chars(text):
    # Регулярний вираз для знаходження електронних адрес
    pattern = r'\b[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Z|a-z]{2,}\b'

    # Функція для заміни символів у знайдених електронних адресах на "-"
    def replace_chars(match):
        return re.sub(r'[^A-Za-z0-9]', '-', match.group(0))

    # Заміна символів у знайдених електронних адресах
    modified_text = re.sub(pattern, replace_chars, text)

    return modified_text


