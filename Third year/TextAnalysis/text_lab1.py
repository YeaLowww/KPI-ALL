def find_emails(text):
    # Регулярний вираз для пошуку електронних адрес
    pattern = r'\b[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Z|a-z]{2,}\b'

    # Знаходимо всі входження
    emails = re.findall(pattern, text)

    return emails
